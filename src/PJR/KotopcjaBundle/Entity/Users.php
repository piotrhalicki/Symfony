<?php

namespace PJR\KotopcjaBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Users
 *
 * @ORM\Table()
 * @ORM\Entity(repositoryClass="PJR\KotopcjaBundle\Entity\UsersRepository")
 */
class Users
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="user_first_name", type="string", length=255)
     */
    private $userFirstName;

    /**
     * @var string
     *
     * @ORM\Column(name="user_last_name", type="string", length=255)
     */
    private $userLastName;

    /**
     * @var string
     *
     * @ORM\Column(name="user_login", type="string", length=255)
     */
    private $userLogin;

    /**
     * @var string
     *
     * @ORM\Column(name="user_email", type="string", length=255)
     */
    private $userEmail;

    /**
     * @var string
     *
     * @ORM\Column(name="user_password", type="string", length=255)
     */
    private $userPassword;

    /**
     * @var string
     *
     * @ORM\Column(name="user_city", type="string", length=255)
     */
    private $userCity;

    /**
     * @var string
     *
     * @ORM\Column(name="user_street", type="string", length=255)
     */
    private $userStreet;

    /**
     * @var string
     *
     * @ORM\Column(name="user_postal", type="string", length=255)
     */
    private $userPostal;

    /**
     * @var string
     *
     * @ORM\Column(name="user_telephone_number", type="integer")
     */
    private $userTelephoneNumber;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set userFirstName
     *
     * @param string $userFirstName
     * @return Users
     */
    public function setUserFirstName($userFirstName)
    {
        $this->userFirstName = $userFirstName;

        return $this;
    }

    /**
     * Get userFirstName
     *
     * @return string 
     */
    public function getUserFirstName()
    {
        return $this->userFirstName;
    }

    /**
     * Set userLastName
     *
     * @param string $userLastName
     * @return Users
     */
    public function setUserLastName($userLastName)
    {
        $this->userLastName = $userLastName;

        return $this;
    }

    /**
     * Get userLastName
     *
     * @return string 
     */
    public function getUserLastName()
    {
        return $this->userLastName;
    }

    /**
     * Set userLogin
     *
     * @param string $userLogin
     * @return Users
     */
    public function setUserLogin($userLogin)
    {
        $this->userLogin = $userLogin;

        return $this;
    }

    /**
     * Get userLogin
     *
     * @return string 
     */
    public function getUserLogin()
    {
        return $this->userLogin;
    }

    /**
     * Set userEmail
     *
     * @param string $userEmail
     * @return Users
     */
    public function setUserEmail($userEmail)
    {
        $this->userEmail = $userEmail;

        return $this;
    }

    /**
     * Get userEmail
     *
     * @return string 
     */
    public function getUserEmail()
    {
        return $this->userEmail;
    }

    /**
     * Set userPassword
     *
     * @param string $userPassword
     * @return Users
     */
    public function setUserPassword($userPassword)
    {
        $this->userPassword = $userPassword;

        return $this;
    }

    /**
     * Get userPassword
     *
     * @return string 
     */
    public function getUserPassword()
    {
        return $this->userPassword;
    }

    /**
     * Set userCity
     *
     * @param string $userCity
     * @return Users
     */
    public function setUserCity($userCity)
    {
        $this->userCity = $userCity;

        return $this;
    }

    /**
     * Get userCity
     *
     * @return string 
     */
    public function getUserCity()
    {
        return $this->userCity;
    }

    /**
     * Set userStreet
     *
     * @param string $userStreet
     * @return Users
     */
    public function setUserStreet($userStreet)
    {
        $this->userStreet = $userStreet;

        return $this;
    }

    /**
     * Get userStreet
     *
     * @return string 
     */
    public function getUserStreet()
    {
        return $this->userStreet;
    }

    /**
     * Set userPostal
     *
     * @param string $userPostal
     * @return Users
     */
    public function setUserPostal($userPostal)
    {
        $this->userPostal = $userPostal;

        return $this;
    }

    /**
     * Get userPostal
     *
     * @return string 
     */
    public function getUserPostal()
    {
        return $this->userPostal;
    }

    /**
     * Set userTelephoneNumber
     *
     * @param integer $userTelephoneNumber
     * @return Users
     */
    public function setUserTelephoneNumber($userTelephoneNumber)
    {
        $this->userTelephoneNumber = $userTelephoneNumber;

        return $this;
    }

    /**
     * Get userTelephoneNumber
     *
     * @return integer 
     */
    public function getUserTelephoneNumber()
    {
        return $this->userTelephoneNumber;
    }
}
